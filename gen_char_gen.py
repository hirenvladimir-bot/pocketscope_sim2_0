# ============================================================================
# Write char_gen.v
# ============================================================================
def write_char_gen(output_path):
    lines = []
    lines.append('`timescale 1ns / 1ps')
    lines.append('// Simple 8x8 Character Generator for VGA text overlay')
    lines.append('// ASCII 0x20-0x7E supported (95 characters, 760 bytes)')
    lines.append('// Font ROM: 768 bytes (96 chars × 8 rows), 0x7F maps to space')
    lines.append('')
    lines.append('module char_gen')
    lines.append('(')
    lines.append('    input  wire          clk,')
    lines.append('    input  wire [7:0]    char_code,     // ASCII character')
    lines.append('    input  wire [2:0]    char_row,      // row within char (0-7)')
    lines.append('    input  wire [2:0]    char_col,      // column within char (0-7)')
    lines.append('    output wire          pixel_on')
    lines.append(');')
    lines.append('')
    lines.append('    // 8x8 font ROM: 96 characters (0x20-0x7F), 8 bytes each = 768 bytes')
    lines.append('    reg [7:0] font_rom [0:767];')
    lines.append('')
    lines.append('    //=====================================================================')
    lines.append('    // Font bitmap data — each character is 8 bytes (one per row, top→bottom)')
    lines.append('    // Each byte: bit7=leftmost pixel, bit0=rightmost pixel')
    lines.append('    //=====================================================================')
    lines.append('')

    # Write initial block with all 768 entries
    lines.append('    initial begin')
    for char_code in range(0x20, 0x80):
        if char_code in FONT:
            bitmap = FONT[char_code]
        else:
            bitmap = FONT[0x20]  # space for undefined
        base = (char_code - 0x20) * 8
        ch_repr = chr(char_code) if 0x20 <= char_code < 0x7F else 'DEL'
        lines.append(f'        // ---- 0x{char_code:02X} ({ch_repr}) ----')
        for row in range(8):
            idx = base + row
            lines.append(f'        font_rom[{idx}]=8\'h{bitmap[row]:02X};')
    lines.append('    end')
    lines.append('')
    lines.append('    //=====================================================================')
    lines.append('    // ROM address: char_idx = (char_code - 0x20), bounded to 0-95')
    lines.append('    // Supports 0x20-0x7F (96 characters). Out-of-range codes map to 0x20.')
    lines.append('    //=====================================================================')
    lines.append('    wire [7:0] cc_sub = char_code - 8\'h20;')
    lines.append('    wire [6:0] char_idx = (char_code < 8\'h20 || char_code > 8\'h7E) ? 7\'d0 : cc_sub[6:0];')
    lines.append('    wire [9:0] rom_addr = {char_idx, char_row};     // char_idx * 8 + row')
    lines.append('    wire [7:0] row_data = font_rom[rom_addr];')
    lines.append('')
    lines.append('    // Output: extract the column bit (MSB = leftmost pixel)')
    lines.append('    assign pixel_on = row_data[7 - char_col];')
    lines.append('')
    lines.append('endmodule')

    with open(output_path, 'w', encoding='utf-8') as f:
        f.write('\n'.join(lines))
    print(f'Wrote {len(lines)} lines to {os.path.basename(output_path)}')

