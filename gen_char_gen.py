# Standard 8x8 font bitmaps — each character is 8 bytes (one per row, top to bottom)
# Each byte: bit7=leftmost pixel, bit0=rightmost pixel
FONT = {}

# Helper: encode a visual 8x8 grid where '#' = pixel on, '.' = pixel off
def make_char(rows):
    """Convert list of 8 strings ('#'/'.) to 8 bytes."""
    result = []
    for row in rows:
        val = 0
        for i, ch in enumerate(row):
            if ch == '#':
                val |= (1 << (7 - i))
        result.append(val)
    return result

